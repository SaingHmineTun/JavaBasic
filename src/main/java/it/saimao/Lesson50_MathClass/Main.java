package it.saimao.Lesson50_MathClass;

public class Main {
    public static void main(String[] args) {
        Math.abs(-5); // 5
        Math.min(100, 50); // 50
        Math.max(100, 50); // 100
        Math.sqrt(25); // 5
        Math.pow(5, 5); // 25
        Math.ceil(1.9); // 1.0
        Math.floor(1.1); // 2.0
        Math.round(1.4); // 1.0
        Math.round(1.6); // 2.0
    }
}
