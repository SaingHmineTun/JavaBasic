package it.saimao.Lesson6_ArithmeticOperators;

public class Main {
    public static void main(String[] args) {
        // 1. Arithmetic Operators
        // + , - , * , / , %
        int sum = 5 + 3; // မၢႆလေႃး
        System.out.println(sum);

        int remain = 5 - 3; // မၢႆထွၼ်
        System.out.println(remain);

        int multiplication = 5 * 3; // မၢႆၵုၼ်း
        System.out.println(multiplication);

        double division = 5 / 3.0; // မၢႆၸႅၵ်ႇ
        System.out.println(division);

        int modulus = 4 % 3; // မၢႆၸႅၵ်ႇလိူဝ်
        System.out.println(modulus);

    }
}
